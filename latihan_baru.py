hari = int(input("masukkan nilai X = "))

if (hari == 0):
	print("Minggu")
elif (hari == 1):
	print("Senin")
elif (hari == 2):
	print("Selasa")
elif (hari == 3):
	print("Rabu")
elif (hari == 4):
	print("Kamis")
elif (hari == 5):
	print("Jum'at")
elif (hari == 6):
	print("Sabtu")
else:
	print("Kode salah")




